  if( oc != 1 ) oc = 0;

 /* инициализируем библиотеку */
  if( !ak_libakrypt_create( ak_function_log_stderr )) return ak_libakrypt_destroy();

 /* устанавливаем нужный вариант совместимости и пересчитываем внутренние таблицы */
  ak_libakrypt_set_openssl_compability( oc );
  /* вместо
      ak_libakrypt_set_option( "openssl_compability", oc );
      ak_bckey_context_kuznechik_init_gost_tables();        */

  oc ? printf("openssl_compability is ON\n") : printf("openssl_compability is OFF\n");

 /* создаем секретный ключ алгоритма Кузнечик */
  if( ak_bckey_context_create_kuznechik( &bkey ) != ak_error_ok )
    return ak_libakrypt_destroy();
 /* создаем секретный ключ алгоритма Магма */
  if( ak_bckey_context_create_magma( &mkey ) != ak_error_ok )
    return ak_libakrypt_destroy();

 /* устанавливаем секретный ключ */
  ak_bckey_context_set_key( &bkey, oc ? openssl_key : key, sizeof( key ));
 /* устанавливаем секретный ключ */
  ak_bckey_context_set_key( &mkey, oc ? openssl_magma_key : magma_key, sizeof( magma_key ));

 /* тестируем алгоритм Кузнечик */
  printf("kuznechik\n");

 /* зашифровываем и расшифровываем всего четыре блока данных в режиме гаммирования */
  ak_bckey_context_ctr( &bkey, oc ? openssl_in : in, buf, sizeof( in ),
                                                                   oc ? openssl_ivctr : ivctr, 8 );
  printf("encrypted:\n");
  for( i = 0; i < 4; i++ ) {
    for( j = 0; j < 16; j++ ) printf(" %02x", buf[i*16+j] );
    printf("\n");
  }
  printf(" %02x %02x %02x\n", buf[64], buf[65], buf[66] );

  ptr = oc ? openssl_outctr : outctr;
  printf("\nexpected:\n");
  for( i = 0; i < 4; i++ ) {
    for( j = 0; j < 16; j++ ) printf(" %02x", ptr[i*16+j] );
    printf("\n");
  }
  printf(" %02x %02x %02x\n", ptr[64], ptr[65], ptr[66] );

  if( ak_ptr_is_equal( buf, ptr, 67 )) {
    printf("Ok\n\n");
    result = EXIT_SUCCESS;
  }  else printf("Wrong\n\n");


 /* тестируем алгоритм Магма */
  printf("magma\n");

