 /* устанавливаем флаг совместимости с openssl: 0 - нет совместимости, 1 - есть */
  int i, j, oc = 0;
  ak_uint8 buf[128], *ptr;
  struct bckey bkey, mkey;

 /* значение секретного ключа согласно ГОСТ Р 34.12-2015 для алгоритма Кузнечик */
